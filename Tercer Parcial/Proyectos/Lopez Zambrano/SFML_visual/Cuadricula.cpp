////////////////////////////////////////////////////////////////////////
// Name:       Cuadricula::setEspacio(Cuadricula* posicion)
// Purpose:    Implementation of Cuadricula::setEspacio()
// Parameters:
// - posicion
// Return:     void
////////////////////////////////////////////////////////////////////////

void Cuadricula::setEspacio(Cuadricula* posicion)
{
	color = posicion->getColor();
	pieza = posicion->getPieza();
}


// Name:       Cuadricula::setVacio()
// Purpose:    Implementation of Cuadricula::setVacio()
// Return:     int
////////////////////////////////////////////////////////////////////////

void Cuadricula::setVacio()
{
	color = NINGUNO;
	pieza = VACIO;
}

////////////////////////////////////////////////////////////////////////
// Name:       Cuadricula::setPiezaYColor(Pieza p, Color c)
